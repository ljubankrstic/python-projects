def move_down(table,column,n_hash):
    if n_hash[column]!=0:
        table[0][column]=0
    pos=0
    len_col=len([x[column] for x in table])
    if n_hash[column]>len_col:
        n_hash[column]=len_col
    curr_n=0
    while curr_n<n_hash[column]:
        if pos+1>=len_col or table[pos+1][column]=="#":
            table[pos][column]="#"
            pos=0
            curr_n+=1
        else:
            table[pos+1][column]=0
            table[pos][column]="*"
            pos+=1
        yield table

def printy(table,n_hash):
    for i in range(len(table[0])):
        t=table
        for k in move_down(t,i,n_hash):
            os.system("clear")
            print_table(k)
            t=k
            time.sleep(.04)
